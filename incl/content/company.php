  <p>
    Wijnwinkel.nl ontwikkelt zich tot de grootste wijnportal op internet. Naast een zorgvuldige selectie wijnen uit de oude wereld is Wijnwinkel.nl met name gespecialiseerd in wijn uit de Nieuwe Wereld. Landen als Zuid-Afrika, Australië, Nieuw-Zeeland, Chili en Argentinië produceren uitstekende wijnen, die bij het grote publiek nog onbekend zijn. Op Wijnwinkel.nl vindt u een zorgvuldig opgebouwd assortiment, geselecteerd op basis van kwaliteit, prijs en exclusiviteit. 
    Naast de mogelijkheid tot bestellen van uitstekende wijnen is Wijnwinkel.nl de meest complete site met informatie over alles wat met wijn te maken heeft. Accessoires, informatie over wijngebieden enzovoort. 
    Heeft u suggesties of opmerkingen dan horen wij die graag van u. Ook als u een speciale wijn zoekt kunt u ons mailen, dan kunnen wij deze voor u proberen te vinden.
